# Example 1: Boolean literals
print("Example 1: Boolean literals")
print(True)      # True
print(False)     # False
print(type(True))  # <class 'bool'>
print(type(False)) # <class 'bool'>

# Example 2: Boolean from data types
print("\nExample 2: Boolean from data types")
print(bool(1))      # True
print(bool(0))      # False
print(bool("Hello")) # True
print(bool(""))     # False
print(bool([]))     # False
print(bool([1,2]))  # True