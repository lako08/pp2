class Dog:
    def bark(self):
        print("Woof!")
    
    def sit(self):
        print("The dog sits.")

class Car:
    def start(self):
        print("Car started")
    
    def stop(self):
        print("Car stopped")

my_dog = Dog()
my_dog.bark()
my_dog.sit()

my_car = Car()
my_car.start()
my_car.stop()